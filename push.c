#include "push_swap.h"

void	push(t_lst **lstb, t_lst *lsta)
{
	t_lst	*lstmp;
	
	if (lstb)
	{
		lst_add(lstb,lsta);
		lstmp = lsta;
		lsta = lsta->next;
		free(lstmp);
	}
	lstmp = NULL;
}
