#include "push_swap.h"

void	ft_printlst(t_lst *lst1)
{
	while(lst1)
	{
		ft_putnbr(((t_numb*)(lst1)->content)->val);
		ft_putstr("\n");
		lst1 = lst1->next;
	}
}
