#include "push_swap.h"

int		ft_p5c(int ac, char **av)
{
	if (ft_checkspacestring(av[ac - 1]) == 1)
	{
		if (ft_checkint(av[ac - 1]) == 0)
		{
			write(2, "Error\n", 6);
			return (0);
		}
	}
	return (1);
}
