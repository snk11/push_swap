#include "push_swap.h"

int		ft_checkoption(char *s, t_option *option)
{
	char	*s1;

	s1 = s;
	while (*s && (*s == ' ' || *s == '\t'))
		s++;
	if (s[0] && s[0] == '-' && s[1] && s[1] == 'c'
			&& (!s[2] || (s[2] && (s[2] == ' ' || s[2] == '\t'))))
		option->indc = 1;
	s = s1;
	while (*s && (*s == ' ' || *s == '\t'))
		s++;
	if (s[0] && s[0] == '-' && s[1] && s[1] == 'f'
			&& (!s[2] || (s[2] && (s[2] == ' ' || s[2] == '\t'))))
		option->indf = 1;
	s = s1;
	while (*s && (*s == ' ' || *s == '\t'))
		s++;
	if (s[0] && s[0] == '-' && s[1] && s[1] == 'l'
			&& (!s[2] || (s[2] && (s[2] == ' ' || s[2] == '\t'))))
		option->indl = 1;
	if (option->indc == 1 || option->indf == 1 || option->indl == 1)
		return (1);
	return (0);
}
