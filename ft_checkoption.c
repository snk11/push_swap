#include "push_swap.h"

void	ft_checkoption(char *s, t_option *option)
{
	while (*s && (*s == ' ' || *s == '\t'))
		s++;
	if (s[0] && s[0] == '-' && s[1] && s[1] == 'c' && (!s[2] || (s[2] && (s[2] == ' ' || s[2] == '\t'))))
		option->indc = 1;

}
