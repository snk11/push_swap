#include "libft.h"

char	*ft_strtrim(char const *s)
{
	int		n;

	if (s == NULL)
		return (NULL);
	while (*s && (*s == ' ' || *s == '\n' || *s == '\t'
				|| *s == '\v' || *s == '\r' || *s == '\f'))
	{
		s++;
	}
	n = ft_strlen(s) - 1;
	while (s[n] && n > 0 && (s[n] == ' ' || s[n] == '\n' || s[n] == '\t'
				|| s[n] == '\v' || s[n] == '\r' || s[n] == '\f'))
	{
		n--;
	}
	return (ft_strsub(s, 0, n + 1));
}
