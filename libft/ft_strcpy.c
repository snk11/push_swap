#include "libft.h"

char	*ft_strcpy(char *s1, const char *s2)
{
	int i;

	if (s2 == NULL)
		return (NULL);
	i = 0;
	while (s2[i])
	{
		s1[i] = s2[i];
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
