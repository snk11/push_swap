#include "push_swap.h"

void	swap5(t_lst ******lsta)
{
	t_lst	*lstmp;

	lstmp = NULL;
	if (*****lsta && (*****lsta)->next)
	{
		lstmp = (*****lsta)->next;
		(*****lsta)->next = ((*****lsta)->next)->next;
		lstmp->next = *****lsta;
		*****lsta = lstmp;
	}
}
